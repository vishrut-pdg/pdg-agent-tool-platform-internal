"""Guards bun-cache + hardlink dedup invariants for sandbox sessions.

See docs/craft/features/bun-node-modules-dedup.md.
"""

from __future__ import annotations

import os
import time
from uuid import UUID
from uuid import uuid4

import pytest
from kubernetes import client

from onyx.server.features.build.configs import SANDBOX_BACKEND
from onyx.server.features.build.configs import SANDBOX_NAMESPACE
from onyx.server.features.build.configs import SandboxBackend
from onyx.server.features.build.sandbox.base import BUN_CACHE_DIR
from onyx.server.features.build.sandbox.kubernetes.kubernetes_sandbox_manager import (
    KubernetesSandboxManager,
)
from tests.external_dependency_unit.constants import TEST_TENANT_ID
from tests.external_dependency_unit.craft._test_helpers import default_llm_config
from tests.external_dependency_unit.craft.conftest import pod_exec
from tests.external_dependency_unit.craft.conftest import wait_for_pod_deletion

pytestmark = pytest.mark.skipif(
    SANDBOX_BACKEND != SandboxBackend.KUBERNETES,
    reason="Bun dedup tests require SANDBOX_BACKEND=kubernetes.",
)

# Without dedup, 2 sessions cost ~1.7 GB; with dedup it's cache + ~20 MB
# overhead. 1.2 GB catches any regression to full-copy installs.
_MAX_WORKSPACE_BYTES_WITH_TWO_SESSIONS = 1_200 * 1024 * 1024
# Snapshot without node_modules is a few MB; with it, ~150 MB.
_MAX_SNAPSHOT_BYTES = 5 * 1024 * 1024
_TEST_USER_ID = UUID("ee0dd46a-23dc-4128-abab-6712b3f4464c")


def _setup_session(
    manager: KubernetesSandboxManager,
    sandbox_id: UUID,
    session_id: UUID,
) -> None:
    """Create one session workspace inside an existing sandbox pod."""
    manager.setup_session_workspace(
        sandbox_id=sandbox_id,
        session_id=session_id,
        llm_config=default_llm_config(
            api_key=os.environ.get("OPENAI_API_KEY", "test-key"),
        ),
        nextjs_port=None,
        skills_section="",
    )


def _du_bytes(k8s_client: client.CoreV1Api, pod_name: str, path: str) -> int:
    """Return the deduped size of ``path`` (in bytes) using a single du walk."""
    raw = pod_exec(
        k8s_client,
        pod_name,
        SANDBOX_NAMESPACE,
        f"du -sb {path} | awk '{{print $1}}'",
    )
    return int(raw.strip())


def _inode(k8s_client: client.CoreV1Api, pod_name: str, path: str) -> str:
    """Return the inode number of ``path`` inside the sandbox pod."""
    raw = pod_exec(
        k8s_client,
        pod_name,
        SANDBOX_NAMESPACE,
        f"stat -c %i {path}",
    )
    return raw.strip()


@pytest.fixture(scope="function")
def two_session_pod(
    k8s_manager: KubernetesSandboxManager,
    k8s_client: client.CoreV1Api,
):
    """Provision a sandbox with **two** sessions installed via bun.

    Returns ``(sandbox_id, session_a, session_b, pod_name)``.
    """
    sandbox_id = uuid4()
    session_a = uuid4()
    session_b = uuid4()

    info = k8s_manager.provision(
        sandbox_id=sandbox_id,
        user_id=_TEST_USER_ID,
        tenant_id=TEST_TENANT_ID,
        llm_config=default_llm_config(
            api_key=os.environ.get("OPENAI_API_KEY", "test-key"),
        ),
        onyx_pat="ci-test-pat",
    )
    assert info.status.value == "running"
    pod_name = f"sandbox-{str(sandbox_id)[:8]}"

    # Wait until pod is fully ready before exec'ing setup_session_workspace.
    for _ in range(30):
        if k8s_manager.health_check(sandbox_id, timeout=2):
            break
        time.sleep(2)
    else:
        raise RuntimeError("pod never became healthy")

    _setup_session(k8s_manager, sandbox_id, session_a)
    _setup_session(k8s_manager, sandbox_id, session_b)

    try:
        yield sandbox_id, session_a, session_b, pod_name
    finally:
        k8s_manager.terminate(sandbox_id)
        wait_for_pod_deletion(k8s_client, pod_name)


def test_bun_cache_lives_on_workspace_volume(
    k8s_client: client.CoreV1Api,
    pool_session: tuple[UUID, UUID, str],
) -> None:
    """The shared bun cache must be on the same mount as session dirs.

    If the cache lives on the overlay rootfs (e.g. /home/sandbox/.bun) instead
    of the workspace volume, hardlinks silently fall back to copies and the
    whole dedup story collapses.
    """
    _, _, pod_name = pool_session
    cache_mount = pod_exec(
        k8s_client,
        pod_name,
        SANDBOX_NAMESPACE,
        f"stat -c %m {BUN_CACHE_DIR}",
    ).strip()
    sessions_mount = pod_exec(
        k8s_client,
        pod_name,
        SANDBOX_NAMESPACE,
        "stat -c %m /workspace/sessions",
    ).strip()
    assert cache_mount == sessions_mount, (
        f"bun cache must share a mount with /workspace/sessions for hardlinks "
        f"to work; got cache mount={cache_mount!r} sessions mount={sessions_mount!r}"
    )


def test_shared_files_hardlink_across_sessions(
    k8s_client: client.CoreV1Api,
    two_session_pod: tuple[UUID, UUID, UUID, str],
) -> None:
    """A file present in both sessions' node_modules must share an inode.

    This is the strongest guarantee that bun is hardlinking instead of copying.
    We pick ``next/package.json`` because it's a deterministic file present in
    every install of this template.
    """
    _, session_a, session_b, pod_name = two_session_pod

    inode_a = _inode(
        k8s_client,
        pod_name,
        f"/workspace/sessions/{session_a}/outputs/web/node_modules/next/package.json",
    )
    inode_b = _inode(
        k8s_client,
        pod_name,
        f"/workspace/sessions/{session_b}/outputs/web/node_modules/next/package.json",
    )
    assert inode_a == inode_b, (
        f"Expected same inode across sessions (hardlinked install), "
        f"got A={inode_a}, B={inode_b}"
    )


def test_two_sessions_stay_under_dedup_budget(
    k8s_client: client.CoreV1Api,
    two_session_pod: tuple[UUID, UUID, UUID, str],
) -> None:
    """Total /workspace/sessions disk usage must stay under the dedup budget.

    Without dedup, 2 sessions consume ~1.7 GB (2 x 836 MB). With dedup, the
    total is the bun cache plus small per-session overhead — should sit
    comfortably under 1.2 GB.
    """
    _, _, _, pod_name = two_session_pod
    used_bytes = _du_bytes(k8s_client, pod_name, "/workspace/sessions")
    assert used_bytes < _MAX_WORKSPACE_BYTES_WITH_TWO_SESSIONS, (
        f"/workspace/sessions used {used_bytes / 1e6:.0f} MB — "
        f"expected under {_MAX_WORKSPACE_BYTES_WITH_TWO_SESSIONS / 1e6:.0f} MB; "
        f"dedup likely regressed to full-copy installs."
    )


def test_snapshot_excludes_node_modules(
    k8s_manager: KubernetesSandboxManager,
    pool_session: tuple[UUID, UUID, str],
) -> None:
    """``create_snapshot`` must omit ``node_modules`` from the tar.

    Including it would (a) blow up S3 storage by ~100x and (b) break dedup
    on restore, because extracted files get fresh inodes that can't share
    with the destination pod's bun cache.
    """
    sandbox_id, session_id, _ = pool_session
    result = k8s_manager.create_snapshot(sandbox_id, session_id, TEST_TENANT_ID)
    assert result is not None, "snapshot should succeed for populated session"
    assert result.size_bytes < _MAX_SNAPSHOT_BYTES, (
        f"snapshot for one session was {result.size_bytes / 1e6:.1f} MB — "
        f"expected under {_MAX_SNAPSHOT_BYTES / 1e6:.0f} MB. "
        f"node_modules likely got included."
    )


def test_restored_session_hardlinks_to_cache(
    k8s_manager: KubernetesSandboxManager,
    k8s_client: client.CoreV1Api,
    pool_session: tuple[UUID, UUID, str],
) -> None:
    """A restored session must rebuild node_modules via hardlinks, not copies.

    Snapshot omits node_modules; restore runs ``bun install --frozen-lockfile``
    so the restored tree shares inodes with the bun cache. Catching a
    regression here is critical — silent fall-back to copies would mean every
    woken sandbox starts at ~840 MB/session of disk before any work.
    """
    sandbox_id, session_id, pod_name = pool_session
    result = k8s_manager.create_snapshot(sandbox_id, session_id, TEST_TENANT_ID)
    assert result is not None

    # Wipe the session locally to force a real restore.
    k8s_manager.cleanup_session_workspace(sandbox_id, session_id)
    gone = pod_exec(
        k8s_client,
        pod_name,
        SANDBOX_NAMESPACE,
        f"[ -d /workspace/sessions/{session_id} ] && echo PRESENT || echo MISSING",
    )
    assert "MISSING" in gone

    k8s_manager.restore_snapshot(
        sandbox_id=sandbox_id,
        session_id=session_id,
        snapshot_storage_path=result.storage_path,
        tenant_id=TEST_TENANT_ID,
        nextjs_port=None,
        llm_config=default_llm_config(
            api_key=os.environ.get("OPENAI_API_KEY", "test-key"),
        ),
        skills_section="No skills available.",
    )

    # node_modules must exist again after restore.
    have_next = pod_exec(
        k8s_client,
        pod_name,
        SANDBOX_NAMESPACE,
        f"test -f /workspace/sessions/{session_id}/outputs/web/node_modules/next/package.json "
        f"&& echo YES || echo NO",
    ).strip()
    assert have_next == "YES", "restored session must have node_modules rebuilt"

    # And it must hardlink to the cache — same inode as a cache file.
    nlink = pod_exec(
        k8s_client,
        pod_name,
        SANDBOX_NAMESPACE,
        f"stat -c %h /workspace/sessions/{session_id}/outputs/web/"
        f"node_modules/next/package.json",
    ).strip()
    assert int(nlink) >= 2, (
        f"Restored node_modules/next/package.json must be hardlinked to the "
        f"bun cache (link count >= 2), got {nlink}"
    )


def test_agent_added_package_survives_snapshot_restore(
    k8s_manager: KubernetesSandboxManager,
    k8s_client: client.CoreV1Api,
    pool_session: tuple[UUID, UUID, str],
) -> None:
    """Packages the agent adds mid-session must survive a snapshot/restore round-trip.

    `bun add <pkg>` writes the dep to both package.json and bun.lock; the
    snapshot captures both, and `bun install --frozen-lockfile` on restore
    fetches the package (cache miss on a fresh pod is OK — bounded one-time
    network cost) and hardlinks it in.
    """
    sandbox_id, session_id, pod_name = pool_session
    session_web = f"/workspace/sessions/{session_id}/outputs/web"

    pod_exec(
        k8s_client,
        pod_name,
        SANDBOX_NAMESPACE,
        f"cd {session_web} && BUN_INSTALL_CACHE_DIR={BUN_CACHE_DIR} bun add lodash",
    )
    in_lockfile = pod_exec(
        k8s_client,
        pod_name,
        SANDBOX_NAMESPACE,
        f"grep -c '\"lodash\"' {session_web}/bun.lock",
    ).strip()
    assert int(in_lockfile) >= 1, "bun add must write lodash into bun.lock"

    result = k8s_manager.create_snapshot(sandbox_id, session_id, TEST_TENANT_ID)
    assert result is not None
    k8s_manager.cleanup_session_workspace(sandbox_id, session_id)
    k8s_manager.restore_snapshot(
        sandbox_id=sandbox_id,
        session_id=session_id,
        snapshot_storage_path=result.storage_path,
        tenant_id=TEST_TENANT_ID,
        nextjs_port=None,
        llm_config=default_llm_config(
            api_key=os.environ.get("OPENAI_API_KEY", "test-key"),
        ),
        skills_section="No skills available.",
    )
    have_lodash = pod_exec(
        k8s_client,
        pod_name,
        SANDBOX_NAMESPACE,
        f"test -f {session_web}/node_modules/lodash/package.json "
        f"&& echo YES || echo NO",
    ).strip()
    assert have_lodash == "YES", "lodash should be rebuilt after restore via bun.lock"


def test_agent_install_in_one_session_does_not_break_other(
    k8s_client: client.CoreV1Api,
    two_session_pod: tuple[UUID, UUID, UUID, str],
) -> None:
    """Mid-session ``bun add`` in session A must not corrupt session B.

    Workspaces share the cache, not the per-session ``node_modules`` tree.
    Adding a package in one session should leave the other's resolution
    untouched.
    """
    _, session_a, session_b, pod_name = two_session_pod
    session_a_web = f"/workspace/sessions/{session_a}/outputs/web"
    session_b_web = f"/workspace/sessions/{session_b}/outputs/web"

    add_log = pod_exec(
        k8s_client,
        pod_name,
        SANDBOX_NAMESPACE,
        f"cd {session_a_web} && "
        f"BUN_INSTALL_CACHE_DIR={BUN_CACHE_DIR} "
        f"bun add lodash 2>&1 | tail -3",
    )
    assert "lodash" in add_log, f"bun add should succeed; got: {add_log!r}"

    a_has_lodash = pod_exec(
        k8s_client,
        pod_name,
        SANDBOX_NAMESPACE,
        f"test -d {session_a_web}/node_modules/lodash && echo YES || echo NO",
    ).strip()
    assert a_has_lodash == "YES", "session A should have lodash after add"

    b_next = pod_exec(
        k8s_client,
        pod_name,
        SANDBOX_NAMESPACE,
        f"test -f {session_b_web}/node_modules/next/package.json && echo OK || echo BROKEN",
    ).strip()
    assert b_next == "OK", "session B's existing tree should be untouched"
